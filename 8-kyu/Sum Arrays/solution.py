# Функция для суммирования элементов списка чисел
def sum_array(a):
    # Инициализируем переменную для суммы (начинаем с 0)
    total = 0
    # Проходим по каждому элементу в списке
    for num in a:
        # Добавляем число к общей сумме
        total += num
    # Возвращаем итоговую сумму (если список пустой, total остаётся 0)
    return total

## Возвращаем сумму элементов или 0, если список пустой
#   return sum(a) if a else 0
# Условие if a else 0 обрабатывает пустой список (хотя sum([]) и так возвращает 0, но для ясности).